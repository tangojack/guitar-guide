def add_new_chord(transpose):
    chord_names = ['Db Major', 'D Major', 'Eb Major', 'E Major', 'F Major', 'F# Major', 'G Major', 'Ab Major', 'A Major', 'Bb Major', 'B Major']

    chord_name = chord_names[transpose - 1]

    new_chords = {}
    new_chords[chord_name] = []
    for cmaj_chord in chords_cmajor['CMajor']:
        root_position = cmaj_chord['root-position']
        notes = cmaj_chord['notes']
        note_0 = notes[0]
        note_1 = notes[1]
        note_2 = notes[2]

        new_root_position = [root_position[0], root_position[1] + transpose % 24]
        new_note_0 = [note_0[0], note_0[1] + transpose % 24]
        new_note_1 = [note_1[0], note_1[1] + transpose % 24]
        new_note_2 = [note_2[0], note_2[1] + transpose % 24]
        new_notes = [new_note_0, new_note_1, new_note_2]

        new_dict = {
            'root-position' : new_root_position,
            'notes' : new_notes,
            'inversion' : cmaj_chord['inversion']
        }

        new_chords[chord_name].append(new_dict)
    chords.update(new_chords)

